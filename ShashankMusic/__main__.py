import asyncio
import importlib
from pyrogram import idle
from pytgcalls.exceptions import NoActiveGroupCall
import config
from ShashankMusic import LOGGER, app, userbot
from ShashankMusic.core.call import Shashank
from ShashankMusic.misc import sudo
from ShashankMusic.plugins import ALL_MODULES
from ShashankMusic.utils.database import get_banned_users, get_gbanned
from config import BANNED_USERS


async def init():
    if (
        not config.STRING1
        and not config.STRING2
        and not config.STRING3
        and not config.STRING4
        and not config.STRING5
    ):
        LOGGER(__name__).error("Assistant client variables not defined, exiting...")
        exit()
    await sudo()
    try:
        users = await get_gbanned()
        for user_id in users:
            BANNED_USERS.add(user_id)
        users = await get_banned_users()
        for user_id in users:
            BANNED_USERS.add(user_id)
    except:
        pass
    await app.start()
    for all_module in ALL_MODULES:
        importlib.import_module("ShashankMusic.plugins" + all_module)
    LOGGER("ShashankMusic.plugins").info("sᴜᴄᴄᴇssғᴜʟʟʏ ɪᴍᴘᴏʀᴛᴇᴅ ᴀʟʟ ᴍᴏᴅᴜʟᴇs...")
    await userbot.start()
    await Shashank.start()
    try:
        await Shashank.stream_call("https://te.legra.ph/file/39b302c93da5c457a87e3.mp4")
    except NoActiveGroupCall:
        LOGGER("ShashankMusic").error("\x56\x43\x20\x28\x56\x6f\x69\x63\x65\x20\x43\x68\x61\x74\x29\x20\x69\x73\x20\x63\x75\x72\x72\x65\x6e\x74\x6c\x79\x20\x4f\x46\x46\x2e\x0a\x50\x6c\x65\x61\x73\x65\x20\x65\x6e\x61\x62\x6c\x65\x20\x76\x6f\x69\x63\x65\x20\x63\x68\x61\x74\x20\x69\x6e\x20\x79\x6f\x75\x72\x20\x67\x72\x6f\x75\x70\x20\x6f\x72\x20\x63\x68\x61\x6e\x6e\x65\x6c\x20\x74\x6f\x20\x73\x74\x61\x72\x74\x20\x74\x68\x65\x20\x62\x6f\x74\x20\x70\x72\x6f\x70\x65\x72\x6c\x79\x2e\x0a\x57\x61\x69\x74\x69\x6e\x67\x20\x66\x6f\x72\x20\x56\x43\x20\x74\x6f\x20\x62\x65\x20\x74\x75\x72\x6e\x65\x64\x20\x4f\x4e\x2e\x2e\x2e\x20\x42\x6f\x74\x20\x69\x73\x20\x63\x75\x72\x72\x65\x6e\x74\x6c\x79\x20\x6f\x6e\x20\x73\x74\x61\x6e\x64\x62\x79\x2e")
        exit()
    except:
        pass
    await Shashank.decorators()
    LOGGER("ShashankMusic").info("\x4d\x75\x73\x69\x63\x20\x62\x6f\x74\x20\x73\x74\x61\x72\x74\x65\x64\x20\x73\x75\x63\x63\x65\x73\x73\x66\x75\x6c\x6c\x79\x21\x0a\x44\x65\x76\x65\x6c\x6f\x70\x65\x64\x20\x62\x79\x20\x40\x69\x74\x7a\x73\x68\x75\x6b\x6c\x61\x20\x28\x53\x68\x61\x73\x68\x61\x6e\x6b\x20\x53\x68\x75\x6b\x6c\x61\x29\x0a\x45\x6e\x6a\x6f\x79\x20\x74\x68\x65\x20\x73\x6f\x75\x6e\x64\x20\x77\x69\x74\x68\x20\x53\x68\x61\x73\x68\x61\x6e\x6b\x20\x4d\x75\x73\x69\x63")
    await idle()
    await app.stop()
    await userbot.stop()
    LOGGER("ShashankMusic").info("\x53\x68\x61\x73\x68\x61\x6e\x6b\x20\x4d\x75\x73\x69\x63\x20\x42\x6f\x74\x20\x68\x61\x73\x20\x62\x65\x65\x6e\x20\x73\x74\x6f\x70\x70\x65\x64\x20\x67\x72\x61\x63\x65\x66\x75\x6c\x6c\x79\x2e\x0a\x54\x6f\x20\x75\x73\x65\x20\x74\x68\x65\x20\x62\x6f\x74\x20\x61\x67\x61\x69\x6e\x2c\x20\x70\x6c\x65\x61\x73\x65\x20\x72\x65\x73\x74\x61\x72\x74\x20\x6f\x72\x20\x61\x63\x74\x69\x76\x61\x74\x65\x20\x76\x6f\x69\x63\x65\x20\x63\x68\x61\x74\x20\x69\x66\x20\x72\x65\x71\x75\x69\x72\x65\x64\x2e\x0a\x54\x68\x61\x6e\x6b\x20\x79\x6f\x75\x20\x66\x6f\x72\x20\x75\x73\x69\x6e\x67\x20\x53\x68\x61\x73\x68\x61\x6e\x6b\x20\x4d\x75\x73\x69\x63\x21")

